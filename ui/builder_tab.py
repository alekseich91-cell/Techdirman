"""
Модуль ``builder_tab`` — заглушка для вкладки «Создание сметы».

Назначение:
    Подготавливает интерфейс для вкладки, отвечающей за формирование сметы
    непосредственно внутри проекта. В текущей версии содержит только
    заглушку с информационным текстом. В дальнейшем здесь появятся
    инструменты для подбора позиций из каталога, расчёта стоимости и т.п.

Как работает:
    • ``build_builder_tab(page, tab)`` — создаёт базовую компоновку
      вкладки, добавляя метку с описанием. Функция принимает объект
      страницы проекта и виджет вкладки.

Стиль:
    • Нумерованные секции поясняют основные шаги при построении UI.
    • В ключевых местах присутствуют однострочные комментарии.
"""

# 1. Импорт библиотек
from PySide6 import QtWidgets


# 2. Построение вкладки «Создание сметы»
def build_builder_tab(page: object, tab: QtWidgets.QWidget) -> None:
    """Создаёт интерфейс для вкладки «Создание сметы».

    На данном этапе выводит только заглушку. Будущая реализация будет
    включать инструменты для добавления позиций из каталога, настройки
    параметров и формирования полноценной сметы.

    :param page: объект ProjectPage, хранящий состояние приложения
    :param tab: виджет вкладки для размещения элементов
    """
    # 2.1 Создаём вертикальный контейнер
    layout = QtWidgets.QVBoxLayout(tab)
    # 2.2 Добавляем метку-заглушку
    label = QtWidgets.QLabel(
        "Вкладка «Создание сметы» пока не реализована. Здесь будут инструменты для составления сметы."
    )
    label.setWordWrap(True)
    layout.addWidget(label)
    layout.addStretch(1)
    # 2.3 Записываем в лог информацию о построении вкладки
    if hasattr(page, "_log"):
        page._log("Вкладка «Создание сметы» создана (заглушка).")